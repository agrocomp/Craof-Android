package br.com.org.entidade.craof;

public class LocalDestino {

    private Integer id_localDestino;
    private String descricao;
    private Integer id_usuario;

    public Integer getId_localDestino() {
        return id_localDestino;
    }

    public void setId_localDestino(Integer id_localDestino) {
        this.id_localDestino = id_localDestino;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public Integer getId_usuario() {
        return id_usuario;
    }

    public void setId_usuario(Integer id_usuario) {
        this.id_usuario = id_usuario;
    }

}
