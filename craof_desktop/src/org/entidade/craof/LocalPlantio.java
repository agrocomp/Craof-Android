package org.entidade.craof;

public class LocalPlantio {

    private Integer id_localPlantio;
    private String descricao;
    private Integer id_usuario;

    public Integer getId_localPlantio() {
        return id_localPlantio;
    }

    public void setId_localPlantio(Integer id_localPlantio) {
        this.id_localPlantio = id_localPlantio;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public Integer getId_usuario() {
        return id_usuario;
    }

    public void setId_usuario(Integer id_usuario) {
        this.id_usuario = id_usuario;
    }
}
